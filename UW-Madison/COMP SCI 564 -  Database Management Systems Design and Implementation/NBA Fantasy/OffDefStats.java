
public class OffDefStats {
  double ppg;
  double apg;
  double orpg;
  double fgp;
  double ftp;
  double tpp;
  double stlpg;
  double blkpg;
  double tovpg;
  double drpg;

  public OffDefStats() {
    reset();
  }

  public void reset() {
    ppg = -1;
    apg = -1;
    orpg = -1;
    fgp = -1;
    ftp = -1;
    tpp = -1;
    stlpg = -1;
    blkpg = -1;
    tovpg = -1;
    drpg = -1;
  }
}
